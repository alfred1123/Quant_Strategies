CREATE OR REPLACE PROCEDURE BT.INSERT_RESULT(
    IN  IN_STRATEGY_ID       UUID,
    IN  IN_RUN_AT            TIMESTAMPTZ,
    IN  IN_DATA_START        DATE,
    IN  IN_DATA_END          DATE,
    IN  IN_TICKER            TEXT,
    IN  IN_FEE_BPS           NUMERIC,
    IN  IN_METRICS_JSON      JSONB,
    IN  IN_WALK_FORWARD_JSON JSONB,
    IN  IN_USER_ID           TEXT,
    IN  IN_CREATED_AT        TIMESTAMPTZ,
    OUT OUT_SQLSTATE         TEXT,
    OUT OUT_SQLERRMC         TEXT
)
LANGUAGE plpgsql
AS $$
BEGIN
    OUT_SQLSTATE := '00000';
    OUT_SQLERRMC := NULL;

    INSERT INTO BT.RESULT (
        STRATEGY_ID, RUN_AT, DATA_START, DATA_END, TICKER,
        FEE_BPS, METRICS_JSON, WALK_FORWARD_JSON, USER_ID, CREATED_AT
    ) VALUES (
        IN_STRATEGY_ID, IN_RUN_AT, IN_DATA_START, IN_DATA_END, IN_TICKER,
        IN_FEE_BPS, IN_METRICS_JSON, IN_WALK_FORWARD_JSON, IN_USER_ID, IN_CREATED_AT
    );

EXCEPTION
    WHEN OTHERS THEN
        GET STACKED DIAGNOSTICS
            OUT_SQLSTATE = RETURNED_SQLSTATE,
            OUT_SQLERRMC = MESSAGE_TEXT;
END;
$$;
